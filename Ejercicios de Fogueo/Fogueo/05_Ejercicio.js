// --------------------------------------------------------------
// Ejercicio 5: Obtener el número mayor de un arreglo.
// Variante 1 array estático (osea solo usa 4 valores)
// --------------------------------------------------------------

function numeroMayor([a, b, c, d]) {
  return Math.max(a, b, c, d);
}

numeroMayor([5, 6, 70, 8]);
